function openNav() {
  document.getElementById("mysidenav").style.width = "250px";
  document.getElementById("sidenavoverlay").style.display = "block";
}
  
function closeNav() {
  document.getElementById("mysidenav").style.width = "0";
  document.getElementById("sidenavoverlay").style.display = "none";
}

function openTab() {
  document.getElementById("tab").style.width = "320px";
  document.getElementById("taboverlay").style.display = "block";
}
  
function closeTab() {
  document.getElementById("tab").style.width = "0";
  document.getElementById("taboverlay").style.display = "none";
}